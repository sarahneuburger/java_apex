package exemplo07;

public class Contagem {

	public static void main(String[] args) {
		
		// While
		// Índice
		
		int indice = 1;
		
		// Laço 1
		while(indice < 11) {
			System.out.println(indice);
			indice++;
		}
				
		// Laço 2
		do {
			System.out.println(indice);
			indice--;			
		} while(indice > 0);
	
		
		// breakpoint - para parar o código e analisar passa a passo, dois cliques na linha
	}
}

package br.com.apex.aula01;

public class CorrecaoExercicio {
	
	// Correção exercício impressão X (exercício 10)
	
	public static void main(String[] args) {
		
		for (int indice = 0; indice < 5; indice++) {
			for(int j = 0; j <= indice; j++) {
				System.out.print("X");
			}
			System.out.println();
		}
	}
}

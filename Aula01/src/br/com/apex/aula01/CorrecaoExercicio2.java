public class CorrecaoExercicio2 {

	public static void main(String[] args) {
		// Correção exercício impressão X (exercício 11)
		
		
		for (int indice = 5; indice >= 0; indice--) {
			for(int j = 0; j <= indice; j++) {
				System.out.print("$");
			}
			System.out.println();
		}
		

	}

}
